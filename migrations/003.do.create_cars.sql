CREATE TABLE cars (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  make TEXT NOT NULL,
  model TEXT NOT NULL,
  vin TEXT,
  vin_short INTEGER
    REFERENCES vinmake(id) ON DELETE CASCADE,
  vin_make INTEGER
    REFERENCES vinmake(id) ON DELETE CASCADE,
  user INTEGER
    REFERENCES users(id) ON DELETE CASCADE NOT NULL,
  date_created TIMESTAMP DEFAULT now() NOT NULL
)